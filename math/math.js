const sum = (a, b) => {
    return a + b}

const sub = (a, b) => {
    return a - b}

const mult = (a, b) => {
    return a * b}

const div = (a, b) => {
    return a/b }

const sqrt = Math => {
    return Math.toSqrt();}

const max = (Math) => {
    return Math.toMax();}

module.exports = {
    sum,
    sub,
    mult,
    div,
    sqrt,
    max
}